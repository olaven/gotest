package calculator

//Add adds given numbers togheter
func Add(a, b int) int {

	return a + b
}

//Subtract subtracts second argument from the first
func Subtract(a, b int) int {

	return a - b
}

//Multiply multiplies given numbers
func Multiply(a, b int) int {

	return a * b
}

//Divide divides first argument by the second
func Divide(a, b float64) float64 {
	return a / b
}
